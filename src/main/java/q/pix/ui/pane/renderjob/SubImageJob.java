package q.pix.ui.pane.renderjob;

public class SubImageJob {
	private int x;
	private int y;
	private int width;
	private int height;
	
	public void run() {
		
	}
	
	public int getX() {
		return x;
	}
	public SubImageJob setX(int x) {
		this.x = x;
		return this;
	}
	public int getY() {
		return y;
	}
	public SubImageJob setY(int y) {
		this.y = y;
		return this;
	}
	public int getWidth() {
		return width;
	}
	public SubImageJob setWidth(int width) {
		this.width = width;
		return this;
	}
	public int getHeight() {
		return height;
	}
	public SubImageJob setHeight(int height) {
		this.height = height;
		return this;
	}
	
	
	
}